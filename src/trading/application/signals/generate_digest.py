"""GenerateDigest use case.

A daily *digest* — a full frontier-model report covering portfolio analytics,
the Congressional signal, and a concrete plan for deploying uninvested cash.
Richer than the congressional-only briefing; generated by a frontier model via
OpenRouter and surfaced as a page on the dashboard, with a Pushover summary
that links to it.

Falls back to a structured template if the model call fails or no OpenRouter
key is configured, so the daily push is never lost.
"""

